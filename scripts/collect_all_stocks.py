def fetch_kline(code, days=60):
    prefix = "sh" if (code.startswith("6") or code.startswith("9") or code.startswith("5")) else "sz"
    url = f"http://money.finance.sina.com.cn/quotes_service/api/json_v2.php/CN_MarketData.getKLineData?symbol={prefix}{code}&scale=240&datalen={days}"
    req = urllib.request.Request(url, headers={"User-Agent":"Mozilla/5.0","Referer":"https://finance.sina.com.cn"})
    try:
        with urllib.request.urlopen(req, timeout=10) as resp:
            raw = json.loads(resp.read().decode("utf-8"))
        if not raw or isinstance(raw, dict):
            return None
        klines = []
        for bar in raw:
            klines.append({
                "date": bar["day"][:10].replace("-",""),
                "open": float(bar["open"]),
                "close": float(bar["close"]),
                "high": float(bar["high"]),
                "low": float(bar["low"]),
                "volume": float(bar["volume"]),
            })
        return klines
    except:
        return None

