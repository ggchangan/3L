def identify_keypoints(code, date_str, lookback=40):
    """
    识别个股的7种关键点，返回结构化数据
    - 第1类：前高、前低、天量K线高低点、地量K线高低点
    - 第2类：突破点、反转点、中继点
    """
    kl = KL(code)
    if not kl: return []
    idx = DI(date_str, kl)
    if idx < 20: return []
    
    start = max(0, idx - lookback)
    prices = [(k["date"], k["open"], k["close"], k["high"], k["low"], k["volume"]) for k in kl[start:idx+1]]
    
    # 基础数据
    C = [p[2] for p in prices]
    H = [p[3] for p in prices]
    L = [p[4] for p in prices]
    V = [p[5] for p in prices]
    
    keypoints = []
    
    # 计算均量
    vol_ma5 = [sum(V[max(0,i-4):i+1])/5 for i in range(len(V))]
    vol_ma20 = [sum(V[max(0,i-19):i+1])/20 for i in range(len(V))]
    
    for i in range(5, len(prices)):
        d = prices[i][0]
        kp_list = []
        
        # ═══ 第1类关键点 ═══
        
        # ① 前高：当前K线高点 > 前后各5根K线的最高点
        if i >= 5 and i <= len(prices)-3:
            left_high = max(H[i-5:i])
            right_high = max(H[i+1:min(i+4, len(H))])
            if H[i] > left_high and H[i] >= right_high:
                kp_list.append(("前高", round(H[i],2), f"高点{H[i]:.2f}为区间最高"))
        
        # ② 前低：当前K线低点 < 前后各5根K线的最低点
        if i >= 5 and i <= len(prices)-3:
            left_low = min(L[i-5:i])
            right_low = min(L[i+1:min(i+4, len(L))])
            if L[i] < left_low and L[i] <= right_low:
                kp_list.append(("前低", round(L[i],2), f"低点{L[i]:.2f}为区间最低"))
        
        # ③ 天量K线：成交量 > MA20量×2.5
        if V[i] > vol_ma20[i] * 2.5 and vol_ma20[i] > 0:
            mult = round(V[i]/vol_ma20[i], 1)
            direct = "阳" if C[i] > prices[i][1] else "阴"
            kp_list.append(("天量", round(H[i],2) if C[i]>prices[i][1] else round(L[i],2), 
                           f"放量{mult}倍{direct}线"))
        
        # ④ 地量K线：成交量 < MA20量×0.4
        if V[i] < vol_ma20[i] * 0.4 and vol_ma20[i] > 0:
            mult = round(V[i]/vol_ma20[i], 2)
            kp_list.append(("地量", round(C[i],2), f"缩量{mult}倍"))
        
        # ═══ 第2类关键点 ═══
        
        # ⑤ 突破点：放量突破前高
        if i >= 3:
            # 找前一个前高位置
            for j in range(i-1, max(i-15, 0), -1):
                if H[j] < H[i] and V[i] > vol_ma5[i] * 1.5 and C[i] > prices[i][1]:
                    kp_list.append(("突破点", round(H[i],2), f"放量突破前高{H[j]:.2f}"))
                    break
        
        # ⑥ 反转点：放量逆转（吞没/锤头线）
        if i >= 3:
            body1 = abs(C[i-1] - prices[i-1][1])
            body = abs(C[i] - prices[i][1])
            if C[i] > prices[i][1] and prices[i-1][1] > C[i-1]:
                if C[i] > prices[i-1][1] and prices[i][1] < C[i-1]:
                    kp_list.append(("反转点", round(C[i],2), "阳吞没反转"))
            # 锤头线：下影线长+实体小+放量
            shadow = min(prices[i][1], C[i]) - L[i]
            if shadow > body * 2 and body < (H[i]-L[i])*0.3 and V[i] > vol_ma5[i]:
                kp_list.append(("反转点", round(L[i],2), "锤头线反转"))
            # 墓碑：上影线长
            shadow_u = H[i] - max(prices[i][1], C[i])
            if shadow_u > body * 2 and body < (H[i]-L[i])*0.3 and C[i] < prices[i][1]:
                kp_list.append(("反转点", round(H[i],2), "墓碑线反转"))
        
        # ⑦ 中继点：缩量回调支撑
        if i >= 3:
            if V[i] < vol_ma5[i] * 0.85 and C[i] > C[i-1]:
                kp_list.append(("中继点", round(C[i],2), f"缩量企稳"))
        
        for kp_type, price, desc in kp_list:
            keypoints.append({
                "date": d, "type": kp_type, "price": price,
                "desc": desc, "days_ago": idx - prices.index(prices[i])
            })
    
    # 去重：同类型取最近的
    seen = set()
    filtered = []
    for kp in reversed(keypoints):
        key = (kp["type"], kp["days_ago"])
        if key not in seen:
            seen.add(key)
            filtered.append(kp)
    filtered.reverse()
    
    # 只返回近30天内的关键点
    recent = [kp for kp in filtered if kp["days_ago"] <= 30]
    
    return recent

